package entity;

/**
 * The `Faculty` enum represents various faculties at an educational institution.
 * It provides a list of predefined faculty options.
 *
 * Faculties include:
 * - SCSE (School of Computer Science and Engineering)
 * - EEE (School of Electrical and Electronic Engineering)
 * - ADM (School of Art, Design and Media)
 * - NBS (Nanyang Business School)
 * - SSS (School of Social Sciences)
 * - NTU (Nanyang Technological University, representing the institution as a whole)
 *
 * This enum is used to specify the faculty associated with users, camps, and other entities in the system.
 */
public enum Faculty {
    SCSE,
    EEE,
    ADM,
    NBS,
    SSS,
    NTU
}

